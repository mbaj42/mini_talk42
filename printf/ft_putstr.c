#include "ft_printf.h"

int	ft_putstr(char *c)
{
	int	i;
	int	len;

	if (!c)
	{
		write(1, "(null)", 6);
		return (6);
	}
	len = 0;
	i = 0;
	while (c[i] != '\0')
	{
		len += ft_putchar(c[i]);
		i++;
	}
	return (len);
}
